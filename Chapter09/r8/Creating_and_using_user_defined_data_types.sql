--query the stored metadata
SELECT
    CUSTOMER_KEY,
    CUSTOMER_ID,
    CUSTOMER_NAME,
    METADATA
FROM DIM_CUSTOMER;

--query individual metadata attributes using object notation
SELECT
    CUSTOMER_NAME,
    METADATA:EFFECTIVE_START_DT,
    METADATA:IS_CURRENT,
    METADATA:SOURCE_SYSTEM_CD
FROM DIM_CUSTOMER;

