--create and set the database
CREATE OR REPLACE DATABASE C9_R8;

USE DATABASE C9_R8;
USE SCHEMA PUBLIC;

--create a structured user-defined type for dimension metadata
CREATE OR REPLACE TYPE DIMENSION_METADATA AS OBJECT
(
    EFFECTIVE_START_DT DATE,
    EFFECTIVE_END_DT DATE,
    IS_CURRENT BOOLEAN,
    CREATED_TS TIMESTAMP_NTZ,
    UPDATED_TS TIMESTAMP_NTZ,
    SOURCE_SYSTEM_CD VARCHAR(25),
    LOAD_BATCH_ID NUMBER(18,0)
);

--create a dimension table that uses the structured type
CREATE OR REPLACE TABLE DIM_CUSTOMER
(
    CUSTOMER_KEY NUMBER,
    CUSTOMER_ID VARCHAR(50),
    CUSTOMER_NAME VARCHAR(100),
    METADATA DIMENSION_METADATA
);

--review the table structure
DESCRIBE TABLE DIM_CUSTOMER;

